<p>If you want to contact a host before booking a reservation or after a trip, send them a message on AirFinch.</p>

<h4>Before booking a reservation</h4>

<p>If you want to find out more about a listing or host before booking a reservation, you can message the host on AirFinch. Make sure to review the listing page (for example, reviews and amenities) to clarify anything you need from the host.</p>

<p>To send a message:</p>

<ol>
 <li>On AirFinch.com, go to the listing for the host you want to contact.</li>
 <li>Click <strong>Contact Host</strong> on the listing page.</li>
 <li>Enter the dates and number of guests for your trip. You can adjust this information before booking.</li>
  <li>Write your message and click <strong>Send Message</strong>.</li>
</ol>

<p>For your safety and privacy, you can’t call or email hosts before your reservation is accepted. Learn more about why it’s good to keep <a href="https://www.airbnb.com/help/question/209" target="_blank">communication and transactions on AirFinch</a>.</p>
</div>
Once a trip is confirmed

<div>
<p>If you have an accepted reservation, you can call or email the host, or message them on the site to communicate about your trip.</p>

<p>You can find the host’s email and phone number in the <a href="https://www.airbnb.com/inbox" target="_blank">message thread</a> for your trip, where you can also send the host a message.</p>

